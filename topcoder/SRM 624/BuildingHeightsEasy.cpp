class BuildingHeightsEasy {
    public:
    int minimum(int M, vector<int> heights) {
        sort(heights.begin(), heights.end());
        int ret = 3000;
        for (int i = M - 1; i < (int) heights.size(); ++i) {
            int tmp = 0;
            for (int j = i - M + 1; j < i; ++j) {
                tmp += heights[i] - heights[j];
            }
            ret = min(tmp, ret);
        }
        return ret;
    }
};

