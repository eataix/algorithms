class RectangleCoveringEasy {
    public:
    int solve(int holeH, int holeW, int boardH, int boardW) {
        if((boardH>=holeH&&boardW>holeW)||(boardH>holeH&&boardW>=holeW)||(boardH>=holeW&&boardW>holeH)||(boardH>holeW&&boardW>=holeH))  
            return 1;
        else
            return -1;
    }
};

